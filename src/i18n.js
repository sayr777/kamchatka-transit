function interpolate(str, params) {
  if (!params || typeof params !== 'object') return str;
  return Object.entries(params).reduce(
    (out, [k, v]) => out.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v)),
    str,
  );
}

/** Translate a key for a given language code, with English fallback. */
export function t(key, lang, params) {
  const str = T[lang]?.[key] ?? T.en[key] ?? key;
  return interpolate(str, params);
}

/** React hook — reads lang from Zustand store automatically. */
export function useT() {
  const lang = useAppStore((s) => s.lang);
  return (key, params) => t(key, lang, params);
}
