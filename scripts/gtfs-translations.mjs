export function translateStop(name, lang) {
  if (!name || lang === 'ru') return name;
  return STOPS[name]?.[lang] ?? name;
}

export function translateRoute(name, lang) {
  if (!name || lang === 'ru') return name;
  let out = name;
  for (const [ru, tr] of ROUTE_PHRASES[lang] ?? []) {
    out = out.split(ru).join(tr);
  }
  return out;
}

